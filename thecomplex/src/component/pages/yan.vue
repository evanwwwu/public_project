          p.q 從出道到現在，會不會有選擇客戶的困擾？
          p.a 
            | 我沒有用「挑客戶」去想事情，我覺得跟客戶相處就是交朋友，你們會挑朋友......慢慢挑越挑越好嗎？不會。一樣的道理，當大家想挑客戶的時候本身就是個問題了。
            br
            | 我的設計哲學中有一個很重要的東西是溝通，如何透過溝通跟別人學習進而影響創作？成見與無法反思是溝通最大的阻礙。大家比較直覺的「溝通」是指「話術」或「說服術」，但我不是在講這種，話術是種工具，之前有人問我怎麼說服客戶，我聽到就想「我沒有在說服客戶～」如果我講的東西客戶不喜歡，我也會有點失落，可能就不會合作了，但我不會跟你講說「買啦買啦，我的設計現在最紅，你買了一定會中！」那就是話術。
        .part.qa
          p.q 有沒有遇過黑心食品或不環保商品找你來做設計，會覺得自己有責任嗎？而如果是某間公司一些產品被發現是黑心的，可是他找你設計的不是那個部分，又會怎麼思考呢？
          p.a 
            | 有欸，還是有碰過，應該說他不自覺的吧，也是可能是做到最後才發現對方有問題，老實說那種會慢慢去避免，跟他們價值觀選擇不一樣。那如果事先知道那是黑心商品，我不會做。
            br
            | 我覺得賺錢的管道很多，所以那個企業有問題的話，也是會避免。美國的設計圈現在全部都在倡導這件事——設計師不應該做無良的設計，也不要為那些無良的企業設計，你們出來選擇很多，可以不要做那樣的東西。
            br
            | 設計師的能力是在把議題傳達出去，跟刺激人們對一件事情的關注，所以我們也有能力讓大家買黑心商品，這是設計師的能力，我們應不應該？我們不應該。我們既然有能力讓大家關注這件事情，就不應該誤導人們做一些不好的東西，但確定大家都知道每個公司幕後的祕密嗎？不見得都知道事情的全貌，知道的話，當然就不要。
        .part.qa
          p.q 最後，對於台灣設計、美感教育，你有什麼樣的看法嗎？
          p.a 
            | 我們要討論何謂美感？美感到底是什麼？我們希望小孩擁有的美感是什麼？是判斷整齊、比例、分佈嗎？還是所謂透過美感讓思考更先進？還是什麼？
            br
            | 以我自己的成長經驗來說，美感對我來說並不是美，而是一種思想上的突破，比如當年印象派，你說他是印象派，是因為他突破當時繪畫的觀點，所以文藝復興之後，我都覺得美是思想革命，那我們要求小孩的美感教育是不是要教他們思考的方法，或者是判斷事情的方式？
