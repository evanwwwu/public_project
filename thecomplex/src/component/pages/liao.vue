        .part.qa
          p.q 現在社會上很多人在反省台灣對視覺設計不夠重視，可是許多商家或團體並沒有經費負擔這一塊，你會覺得平面設計師應該扮演什麼角色嗎？
          p.a 
          | 我並不認為設計師有這個社會責任要用低價幫人家做東西。如果你覺得他們的故事很感人，東西很好但不知道怎麼推，你有心想要幫助他們，fine，我覺得這個心是很好的，但那並不是設計師的社會責任，設計師不是革命家不是救世主，我們其實就是生意人，我們的確很單純就是生意人。
          br
          | 這件事情還蠻嚴重的，因為設計師是一個軟專業，我們沒有辦法有一個六法全書、會計證照，或是醫師執照來搭配顯得我們很專業，所以這個社會會感覺「你想一想、簡單畫一畫就好了」，或是「這個很簡單我也可以做」。當你無償去幫別人的時候，你等於讓對方確認這件事情：對，設計真的是很簡單就可以拿到，不需要太大代價、付出、努力。這會讓設計師背後的專業素養跟絞盡腦汁的思考越來越不被看見。
        .part.qa
          p.q 這樣是不是會變成一種惡性循環？
          p.a 
            | 你盡到了一時的古道熱腸，但對方可能會跟他的朋友說「你去找那個設計師啊，搞不好他可以免費幫你做，搞不好他想一想，隔天、一個禮拜就可以幫你了。」最後你身邊環繞的都是這樣的案子，客戶更加看不到你那一個禮拜是多絞盡腦汁，看不到你是花了多少年的時間，才能夠濃縮到一個禮拜完成。當十個二十個三十個設計師這樣做的時候，這對設計圈來說是一個很可怕的戕害。
            br
            | 所以我才說，設計師從來沒有責任以極低價的方式幫窮苦的人做稿子，相反的，設計師有責任要讓設計費變得越來越高，使更多的人覺得設計非常的有價值，進而讓設計師有更大的責任義務幫客戶更茁壯，這才是設計圈應該要有的健康成長方向。
        .part.qa
          p.q 專業的設計師值得專業的價格，但專業很難定義，不同專業的程度也會有不同價格，如果一般民眾分辨不出什麼是專業怎麼辦？
          p.a 
            | 每個設計師的價錢當然不太一樣，但其實我本身不是很喜歡學生價。你出來接案的時候，就不應該存著「我是學生」這種僥倖心態，應該以專業人士的角度去解決客戶的所有問題，收一般正常的錢；而相對來說，因為你的經驗不足所產生的任何問題，都應該要絞盡腦汁去解決，想辦法克服，問人啊還是幹嘛的。
            br
            | 如果你以設計師的身份出來接案子，卻沒有一個設計師該有的素養，那設計師應該要捫心自問：還接什麼案啊？好像是你去開機車行卻不會修機車，你開診所有些病你不會看，這是一樣的道理，如果有些病你不會看你是不是應該把它轉介出去，或是不應該開診所。
            br
            | 當你出來接案子的時候，就不再是代表你自己了，而是代表整個設計界——整個台灣的設計界。所以千萬不可以隨隨便便說「好，我幫你！」這句話折損的不是你個人，而是整個設計界。
