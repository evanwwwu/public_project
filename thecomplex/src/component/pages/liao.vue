          p 廖小子個性爽直，聲音與作品一樣宏亮充滿剛勁，有時聽完問題後會先陷入沉思，原以為他的回答會簡練樸直，不料幾秒後，一開口便是奮進的氣勢，歷史脈絡與社會學信手捻來。為了落實節能反核的立場，小子不開冷氣，也不吹電風扇，受訪時他穿黑色吊嘎，滿臉是汗也鮮少見他伸手去擦。
        .part.qa
          p.q 你會覺得你在作品裡加入本土元素是在實踐身為平面設計師的文化責任嗎？
          p.a 
            | 我覺得這是人家強加諸在我身上的，實際上我在一開始做的時候並沒有這樣的意識在裡面。
            br
            | 一開始做的案子跟現在做的類型差異很大，像是言情小說的封面，或小廠牌化妝品的DM，客戶一直狂改稿，希望字可以放很大、顏色很鮮豔、圖要修的很沙龍、鋪個玫瑰，再加上外框與陰影。我本來覺得很痛苦，但之後就想：既然打不過他那就加入他，而要做就要做到自己很滿意的地步。久而久之「把字放很大」、「挑戰字的裝飾性的可能」......等等就變成自己的風格。
            br
            | 另一方面，小時候我家裡是勞工階級，會接觸到那些廟宇啊、招牌啊、卡車噴漆之類的，我在思考「我要找什麼東西來做？」、「我可以怎麼去做？」的時候，會很容易往那些方向去想。這些東西從小跟我一起長大，所以對我來說並沒有先入為主「醜的概念」——我到長大之後才有人跟我講那是醜。
        .part.qa
          p.q 重視自身經驗的挖掘這點，是不是與你在美術系所受的訓練有關？
          p.a 是。從自己的生活、成長背景去想，這樣的思維是美術系給我的教育。我是在那樣的環境裡長大的，所以對我來說自然而然會應用那些元素，我一直在做這樣的事情。後來人家來跟我講這是「庶民風格」，或是什麼「很本土」，被這樣稱呼我也沒有覺得哪裡不好，所以就讓他們繼續這樣講。
        .part.qa
          p.q 被這樣講完之後也沒有什麼改變，就繼續這樣做是嗎？
          p.a 我是被迫樣這樣做的，因為我就在這樣的背景裡長大。而被這麼講之後，我也是會從那些東西再去延伸，更多的去觀察自己的環境。
        .part.qa
          p.q 在之前的報導中你有提到，你會希望透過使用一些台灣街頭中的元素，讓大家回過頭來發現身邊那些元素值得欣賞。可是一個符號讓人感覺親近，不一定會讓人覺得美。
          p.a 
            | 應該說，我用這些元素是希望讓人家覺得台灣街頭有一定可愛的地方。
            br
            | 像我做李英宏那張專輯用噴漆，但之後大家在產業道路上看到「你要工人」的噴漆時，不一定要很驚訝在那邊看半天，左拍右拍看半小時，不用這樣！！（笑） 而是大家會發現那些元素是台灣街道一個可愛的地方，值得珍惜，有更多的可能性，哪一天在新聞上看到有人要把老房子拆掉的時候，不會無感地覺得這是可以的！那些元素大家不一定要買單，但我希望能埋下這樣的種子。
        .part.qa
          p.q 而在另一篇的報導中，你提到中華藝校官網首頁創意無限，怎麼說呢？
          p.a 當時舉中華藝校只是個例子，那種感覺就很像我看到蓋台廣告一樣——我自己能夠把所有元素拆開來，然後再重組。如果我重新組合，說不定可以做出一個前衛的作品，所以在接觸那些作品的時候，我都會以「如何去拆他們」這樣的視角觀看。
